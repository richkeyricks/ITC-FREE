class Translator:
    def __init__(self, lang_code="ID"):
        self.lang_code = lang_code if lang_code in LANGUAGES else "ID"
        self.strings = LANGUAGES[self.lang_code]

    def get(self, key, default=None):
        return self.strings.get(key, default if default is not None else f"[{key}]")

    def set_language(self, lang_code):
        if lang_code in LANGUAGES:
            self.lang_code = lang_code
            self.strings = LANGUAGES[self.lang_code]

    def get_brokers(self):
        """Returns localized top brokers using CENTRALIZED partner data"""
        from constants.partner_data import FOREX_BROKERS
        
        results = []
        for broker in FOREX_BROKERS:
            # Select localized description
            desc = broker.get('desc_id') if self.lang_code == "ID" else broker.get('desc_en')
            if not desc: desc = broker.get('desc', "")
            
            # Map to UI format
            results.append({
                "name": broker['name'],
                "desc": desc,
                "url": broker['url'],
                "is_affiliate": True # All in this list are verified affiliates
            })
        return results

