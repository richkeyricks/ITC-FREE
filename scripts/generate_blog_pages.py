def generate_body(article):
    body = ""
    for section in article["sections"]:
        body += f'<section id="{section["id"]}">\n'
        body += f'    <h2>{section["h2"]}</h2>\n'
        body += f'    <p>{section["p"]}</p>\n'
        if "extra" in section:
            body += f'    <div class="insight-box"><p>{section["extra"]}</p></div>\n'
        body += f'</section>\n'
    return body

def generate_toc_links(article):
    links = ""
    for item in article["toc"]:
        links += f'<li><a href="#{item["id"]}">{item["title"]}</a></li>\n'
    return links

def generate_related_links(current_article, all_articles):
    links = ""
    count = 0
    for art in all_articles:
        if art["filename"] != current_article["filename"] and count < 3:
            links += f'<a href="{art["filename"]}" class="related-card">→ {art["title"]}</a>\n'
            count += 1
    return links

